            <h1>Helper Classes</h1>
            <p>POC provides a few additional classes that can be helpful in certain cases.</p>
            <h3>Quick Float Left</h3>
            <p>Add class="poc-fl-l" to any element to float it to the left. For example:</p>

            <div class="map-form-item">
                <h3>Code</h3>
                <pre>
&lt;div class="poc-fl-l">&lt;/div> </pre>

            </div>
            <!-- end map-form-item-->
            <h3>Quick Float Right</h3>
            <p>Add class="poc-fl-r" to any element to float it to the right. For example:</p>

            <div class="map-form-item">
                <h3>Code</h3>
                <pre>
&lt;div class="poc-fl-r">&lt;/div> </pre>

            </div>
            <!-- end map-form-item-->


            <h3>Clearfix
            </h3>
            <p>If you need to clear floated elements, all you need to include is an empty div with the ".poc-clearfix" class after the last floated item.</p>

            <div class="map-form-item">
                <h3>Code</h3>
                <pre>
&lt;div class="poc-clearfix">&lt;/div> </pre>

            </div>
            <!-- end map-form-item-->

            <h3>No Print - Helper
            </h3>
            <p>If you want elements like images to be hidden when printing your website, just add the ".poc-noPrint" class.</p>

            <div class="map-form-item">
                <h3>Code</h3>
                <pre>
&lt;div class="poc-noPrint">&lt;/div>
            </pre>
            </div>

        </div>
    </div>

